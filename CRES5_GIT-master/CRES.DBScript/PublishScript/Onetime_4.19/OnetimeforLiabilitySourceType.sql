Update cre.liabilitynote set cre.liabilitynote.LiabilitySource = z.sourceID,cre.liabilitynote.UpdatedDate = getdate()
From(

	SELECT Distinct sub.AbbreviationName,d.CREDealID,ln.AccountID as liabilitynoteAccountid,t.Source,l.LookupID as sourceID
	--,d.DealName,ln.LiabilityNoteID,ln.AccountID
	FROM cre.liabilitynote ln
	Inner join cre.deal d on d.AccountID = ln.DealAccountID
	INNER JOIN cre.liabilitynoteassetmapping la ON ln.AccountID = la.LiabilityNoteAccountId
	INNER JOIN (
		SELECT eq.AbbreviationName,l.LiabilityTypeID,am.AssetAccountId AS assetnotesid
		FROM cre.liabilitynote l
		INNER JOIN cre.liabilitynoteassetmapping am ON l.AccountID = am.LiabilityNoteAccountId
		Inner JOin cre.Equity eq on eq.AccountID = l.LiabilityTypeID
   
	   --WHERE l.LiabilityTypeID in (Select accountid from cre.Equity)
	) sub ON la.AssetAccountId = sub.assetnotesid
	LEFT JOIN core.Account a on ln.LiabilityTypeID = a.AccountID
	Left join @tblsource t on t.Fund = sub.AbbreviationName and t.DealId = d.CREDealID
	left join core.lookup l on l.name = t.Source
	where a.IsDeleted <> 1
	--and d.DealName = '45-03 23rd Street'
	--order by sub.AbbreviationName,d.CREDealID

)z
Where cre.liabilitynote.AccountID = z.liabilitynoteAccountid


